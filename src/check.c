int are_valid_3_letter_combos(char* str, int strlen) {
	for(int i = 0; i < strlen - 2; i++) {
		if(str[i] < 'a' || str[i] > 'z') {
			continue;
		}

		int index = (int)str[i] - 'a';

		if(!in_char_array(valid_3_letter_combos[index][(int)str[i + 1] - 'a'], str[i + 2])) {
			return 0;
		}
		continue;
	}

	return 1;
}
