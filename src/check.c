int are_valid_2_letter_combos(char* str, int strlen) {
	for(int i = 0; i < strlen - 1; i++) {
		if(str[i] < 'a' || str[i] > 'z') {
			continue;
		}

		if(in_char_array(invalid_2_letter_combos[(int)str[i] - 'a'], str[i + 1])) {
			return 0;
		}
	}

	return 1;
}

